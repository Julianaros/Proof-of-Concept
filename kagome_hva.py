# 9. Fine-grained analysis: spin-spin correlations and entanglement entropy
# --------------------------------------------------------------------------
def _apply2_sv(n, psi, G, a, b):
    """Applies a 4x4 matrix G to qubits (a,b) of a statevector reshaped to (2,)*n."""
    axa, axb = n - 1 - a, n - 1 - b
    psi = np.moveaxis(psi, [axa, axb], [0, 1])
    sh = psi.shape
    psi = (G @ psi.reshape(4, -1)).reshape(sh)
    return np.moveaxis(psi, [0, 1], [axa, axb])


def pair_correlations(n, sv, pairs):
    """
    Correlations ⟨S_i·S_j⟩ = ⟨X_iX_j+Y_iY_j+Z_iZ_j⟩/4 for each pair (i,j), computed
    term by term on the statevector (light: no global operators are built).
    Returns {(i,j): value}. Pure singlet: -3/4; uncorrelated: 0.
    """
    psiT = np.asarray(sv).reshape((2,) * n)
    out = {}
    for (i, j) in pairs:
        b = _apply2_sv(n, psiT.copy(), _BOND, i, j).reshape(-1)
        out[(i, j)] = float(np.real(np.vdot(np.asarray(sv).reshape(-1), b))) / 4.0
    return out


def entanglement_entropy_cuts(n, sv):
    """
    Von Neumann entropy S(k) (in bits) of the qubit block {0..k-1} for k = 1..n-1,
    via SVD of the reshaped statevector. A cut that splits a singlet contributes
    1 bit; a product state gives 0. Note: the "block {0..k-1}" refers to the tensor
    axes (Qiskit qubit order).
    """
    v = np.asarray(sv).reshape((2,) * n)
    ent = []
    for k in range(1, n):
        m = v.reshape(2 ** k, 2 ** (n - k))
        s = np.linalg.svd(m, compute_uv=False)
        p = s ** 2
        p = p[p > 1e-14]
        ent.append(float(-(p * np.log2(p)).sum()))
    return ent
